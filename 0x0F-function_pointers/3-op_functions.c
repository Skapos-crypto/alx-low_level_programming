#include "3-calc.h"

/**
 * op_add - the sum of a and b
 * @a: Num 1
 * @b: Num 2
 * Return: returns the sum of a and b
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - the difference of a and b
 * @a: Num 1
 * @b: Num 2
 * Return: returns the difference of a and b
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - the product of a and b
 * @a: Num 1
 * @b: Num 2
 * Return: returns the product of a and b
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - the result of the division of a by b
 * @a: Num 1
 * @b: Num 2
 * Return: returns the result of the division of a by b
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - the remainder of the division of a by b
 * @a: Num 1
 * @b: Num 2
 * Return: returns the remainder of the division of a by b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
